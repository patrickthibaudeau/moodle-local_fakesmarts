namespace local_fakesmarts\output;

use local_fakesmarts\fakesmarts;
use local_fakesmarts\output\type;

class translate implements \renderable, \templatable
{


    public function __construct()
    {

    }

    /**
     *
     * @param \renderer_base $output
     * @return type
     * @global \moodle_database $DB
     * @global type $USER
     * @global type $CFG
     */
    public function export_for_template(\renderer_base $output)
    {
        global $USER, $CFG, $DB;

        $config = get_config('local_fakesmarts');

        $data = [
            'bot_id' => $config->translate,
        ];

        return $data;
    }

}
